void solve() {
    int n; cin >> n;
    map<int,int> ct;
    while(n--) {
        int x; cin >> x;
        ct[x]++;
    }
    int ans = 0;
    for(auto [x,y] : ct) {
        ans += y / 3;
    }
    cout << ans << endl;
}
