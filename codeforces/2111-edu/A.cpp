template<class T, class... Args>
auto Vec(size_t n, Args... args) {
    if constexpr(sizeof...(args) == 1)
        return vector<T>(n, args...);
    else
        return vector(n, Vec<T>(args...));
}
void solve() {
    int a[3] = {};
    int x; cin >> x;
    int ans = 0;
    while(a[0] != x || a[1] != x || a[2] != x) {
        sort(a, a+3);
        a[0] = min(x, a[1] * 2 + 1);
        ans ++;
    }
    cout << ans << endl;
}
int main() {
    ios::sync_with_stdio(false);
    cin.tie(nullptr);
    int t; cin>>t;
    while(t--) solve();
    return 0;
}
